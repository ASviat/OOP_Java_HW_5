package Task01;

/**
 * CalcModel
 */
public class CalcModel {

    public double plus(double firstDigit, double secondDigit) {
        return firstDigit + secondDigit;
    }

    public double minus(double firstDigit, double secondDigit) {
        return firstDigit - secondDigit;
    }

    public double multiply(double firstDigit, double secondDigit) {
        return firstDigit * secondDigit;
    }

    public double divide(double firstDigit, double secondDigit) {
        return firstDigit / secondDigit;
    }
}